package Constructor;

public class SwitchCase {

    public static void main(String[] args) {
        int i = 11;
        switch (i) {
            case 0:
                System.out.println("Sunday");
                break;
            case 1:
                System.out.println("Saturday");
                break;
            case 2:
                System.out.println("Saturday");
                break;
            default:
                System.out.println("Another day");
                break;

        }
    }
}
